/************************************************************************/
/* A/D値読み込み(AN2)                                                   */
/* 引数　 なし                                                          */
/* 戻り値 A/D値 0〜1023                                                 */
/************************************************************************/
int check_Maker( void )
{
    int i;

    /* A/Dコンバータの設定 */
	i = i;						/* φADの1サイクルウエイト入れる*/
	AD_CSR |= 0x20;				/* AD スタート */
	while( !(AD_CSR & 0x80) );	/* エンドフラグをチェック */
	AD_CSR &= 0x7f;				/* エンドフラグクリア */
	i = AD_DRA >> 6;			/* 代入 */
	return i;
}
